    /**
     * Login action performed ; Iser name Password Validation 
     * Retrieves User details
     * @param evt 
     */
    private void jButton_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LoginActionPerformed

        try {
            obj_beanClass = new beanClass();

            String varQuery = "SELECT * FROM PROFILE WHERE USERNAME='" + jTextField1_username.getText() + "'";
            ResultSet rst = new DBHelper().getSt().executeQuery(varQuery);
            obj_beanClass.setVarUsername(jTextField1_username.getText());
            obj_beanClass.setVarPassword(String.valueOf(jPasswordField1_Password.getPassword()));

            if ((!jTextField1_username.equals("")) && (!obj_beanClass.getVarPassword().equals(""))) {

                while (rst.next()) {

                    if ((rst.getString("USERNAME").equals(obj_beanClass.getVarUsername())) && (rst.getString("PASSWORD").equals(obj_beanClass.getVarPassword()))) {
                        obj_beanClass.setVarAddress(rst.getString("ADDRESS"));
                        obj_beanClass.setVarCity(rst.getString("CITY"));
                        obj_beanClass.setVarCountry(rst.getString("COUNTRY"));
                        obj_beanClass.setVarEmail(rst.getString("EMAIL"));
                        obj_beanClass.setVarFirst_name(rst.getString("FIRST_NAME"));
                        obj_beanClass.setVarLast_name(rst.getString("LAST_NAME"));
                        obj_beanClass.setVarMobile(rst.getString("MOBILE"));
                        obj_beanClass.setVarPincode(rst.getString("PINCODE"));
                        obj_beanClass.setVarUsername(rst.getString("USERNAME"));

                        jPanel_userFrontEnd.setVisible(true);
                        jPanel_loginScreen.setVisible(false);

                        lbl_Address.setText(obj_beanClass.getVarAddress());
                        lbl_City.setText(obj_beanClass.getVarCity());
                        lbl_Email.setText(obj_beanClass.getVarEmail());
                        lbl_Mobile.setText(obj_beanClass.getVarMobile());
                        lbl_country.setText(obj_beanClass.getVarCountry());
                        lbl_lastname.setText(obj_beanClass.getVarLast_name());
                        lbl_username.setText(obj_beanClass.getVarUsername());
                        lbl_Firstname.setText(obj_beanClass.getVarFirst_name());
                        lbl_Pincode.setText(obj_beanClass.getVarPincode());

                        tree_Function();
                        this.setResizable(true);
                        this.setExtendedState(MAXIMIZED_BOTH);

                    } else {
                        JOptionPane.showMessageDialog(null, "Wrong Username or Password");

                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Username & Password field can't be Empty.");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }


    }//GEN-LAST:event_jButton_LoginActionPerformed

    
