    /**
     * Used to upload files 
     * @param evt 
     */
    private void jButton2_UploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2_UploadActionPerformed
        File file_upload = new File(path_file_used_by_browse);

        //        System.out.println("path"+file_upload);
        if (file_upload.toString().toUpperCase().endsWith("DOCX") || file_upload.toString().toUpperCase().endsWith("DOC")) {
            folder_name = "DOCX";
        } else {
            if (file_upload.toString().toUpperCase().endsWith("TXT")) {
                folder_name = "TXT";
            } else {
                if (file_upload.toString().toUpperCase().endsWith("XLSX") || file_upload.toString().toUpperCase().endsWith("XLSX")) {
                    folder_name = "XLSX";
                } else {
                    if (file_upload.toString().toUpperCase().endsWith("PPTX") || file_upload.toString().toUpperCase().endsWith("PPT")) {
                        folder_name = "PPTX";
                    } else {
                        JOptionPane.showMessageDialog(null, "Files other than Docx,TXT,XLSX,PPTX are not allowed to upload");
                        folder_name = null;

                    }
                }
            }

        }
        /**
         * file_name is the name of variable that we are uploading; to the
         * server
         */
        if (!folder_name.equals(null)) {
            String file_name = file_upload.getName();

            File target = new File(homeDir + obj_beanClass.getVarUsername() + "/" + folder_name + "/" + file_name);

            try {

                Files.copy(file_upload.toPath(), target.toPath(), REPLACE_EXISTING);
                JOptionPane.showMessageDialog(null, "File Uploaded Successfully!" + " to " + folder_name);

            } catch (IOException ex) {

                JOptionPane.showMessageDialog(null, ex);

            }
        }

        jButton2_Upload.setEnabled(false);
    }//GEN-LAST:event_jButton2_UploadActionPerformed

    
    
